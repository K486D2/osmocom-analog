void init_radiocom_tones(void)
{
	int i, j;


	for (i = 0, j = 0; i < 12000; i++) {
		tone[i] = pattern[j++];
		if (j == 200)
			j = 0;
	}

	ringback_spl = tone;
	ringback_size = 12000;
	ringback_max = 8 * 5000; /* 1500 / 3500 */

	busy_spl = tone;
	busy_size = 4000;
	busy_max = 8 * 1000; /* 500 / 500 */

	congestion_spl = tone;
	congestion_size = 4000;
	congestion_max = 8 * 1000; /* 500 / 500 */
}

